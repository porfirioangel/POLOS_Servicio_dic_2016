      municipales, instituciones de educación y empresas de base tecnológica de cada región, focalizando estos esfuerzos para
      beneficio de los sectores mencionados pero de manera prioritaria para el beneficio social de las regiones.
      El Laboratorio de Software Libre (LABSOL) del Consejo Zacatecano de Ciencia, Tecnología e Innovación (COZCyT) adopto
      como una estrategia principal la vinculación y colaboración con instituciones de educación media superior y superior de
      todo el Estado, con esta vinculación se ha convertido en un importante centro de desarrollo tecnológico con un catálogo
      de proyectos desarrollados que supera los 60 y cada semestre se desarrollan cuando menos una docena más; al ser
      proyectos basados en software libre y de código abierto pueden ser aprovechados por cualquiera para su implantación; la
      intención es que los Polos de Innovación Tecnológica colaboren estrechamente con el LABSOL para que dichos proyectos
