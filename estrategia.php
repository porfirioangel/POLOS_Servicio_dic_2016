      <h1 class="text-center">Estrategias</h1>
        <div class="col-md-10 col-md-offset-1">
          <p class="text-justify"> 
            
            Siendo la Innovación Abierta una de las acciones estratégicas más productivas en paises como Brasil en la que el desarrollo
            tecnológico ha sido importante de manera integral para todos los sectores, con la participación responsable de las
            instancias de gobierno y el trabajo comprometido de comunidades de colaboración; la integración de todos los sectores
            productivos además de las instancias públicas y organizaciones al proyecto de los Polos de Innovación Tecnológica es
            importante para garantizar que se cumpla la misión para la que serán creados.
            A continuación se enlistan las principales estrategias sobre las cuales se establecen las acciones de los Polos de Innovación
            Tecnológica en el Estado de Zacatecas:

            <h1>1. Instancias que colaboran y sus funciones.</h1>

            A continuación se definen las instancias y sus funciones que se integran al proyecto:
              <br>
              <h4>Laboratorio de Software Libre del COZCyT</h4>
              <ul>
                <li>Equipar, en base a sus capaciades presupuestales, los Polos</li>
                <li>Analizar a los posibles líderes de los Polos priorizando a estudiantes de instituciones de
              educación que colaboran con el LABSOL</li>
                <li>Preparar los equipos de trabajo de cada Polo de Innovación, preparandolos con temas actuales
              en cuestión de innovación tecnológica</li>
                <li>Compartir todos los proyectos que se han desarrollado como parte de sus estrategias a los Polos
              de Innovación para que sean utilizados de manera libre</li>
                <li>Respaldar y asesorar en todas las acciones que desarrollen los Polos</li>
                <li>Ofrecer de manera permanente programas de capacitación y actualización</li>
                <li>Organizar eventos de difusión y divulgación tecnológica</li>
              </ul>
              <br>
              <h4>Presidencias Municipales</h4>

              <ul>
                <li>Ofrecer un empleo asalariado al líder del Polo</li>
                <li>Otorgar un espacio físico donde implantar los Polos de Innovación Tecnológica</li>
                <li>Otorgar los servicios básicos como energía eléctrica, agua y acceso a internet</li>
                <li>Difundir y divulgar como parte de sus estrategias municipales las acciones de los Polos</li>
                <li>Integrar a los Polos como parte de sus estrategias en temas de desarrollo económico y social</li>
              </ul>
            <br>
            <h4>Instituciones de Educación</h4>

            <ul>
              <li>Difundir entre sus estudiantes los Proyectos que se desarrollan en los Polos</li>
              <li>Integrar a los estudiantes a los Polos de Innovación</li>
              <li>Integrarse a las acciones de capacitación y actualización de los Polos</li>
              <li>Colaborar con los Polos en la organización de eventos de difusión y divulgación</li>
            </ul>
              <br>
              <h4>Empresas de TICs</h4>
              <ul>
                <li>Integrarse al desarrollo de proyectos generados por los Polos</li>
                <li>Aprovechar las convocatorias para gestión de fondos para el desarrollo de proyectos</li>
                <li>Integrarse a los programas de capacitación que organizarán los Polos</li>
              </ul>
            <br><br>
            <h1>2. Infraestructura y servicios</h1>
              <h4>Equipamiento y mobiliario en base a las dimensiones del lugar físico</h4>
              <ul>
                <li>Cubierto en base a presupuesto del LABSOL del COZCyT</li>
                <li>Área común y pública: 10 computadoras e impresora láser a color y su respectivo mobiliario</li>
                <li>Áula de capacitación: Pantalla de proyección, proyector multimedia, sistema de audio y video</li>
              </ul>
                <br>
              <h4>Servicios de Energía Eléctrica, agua potable, sanitario y acceso a Internet de banda ancha</h4>
              <ul>
                <li>Cubierto por las presidencias municipales</li>
                <li>Acceso a Internet de banda ancha de mínimo 2mbps</li>
                <li>Cableado estructurado</li>
                <li>Acceso a Internet Inalámbrico gratuito</li> 
              </ul>
              <br><br>
            <h1>3. Reglas de operación</h1>

            Las reglas de operación que definen las acciones, compromisos, responsabilidades y funciones de los Polos de
            Innovación están basadas en los puntos detallados a lo largo del presente documento, generando un documento
            específico que lo defina como tal; además de considerar puntos relevantes que generen autosuficiencia de los
            Polos como los siguientes:
            <br>
            <h4>Cuotas por servicios y desarrollo de proyectos</h4>

            <ul>
              <li>Desarrollo completo de de Proyectos: 10% del costo total del proyecto</li>
              <li>Desarrollo de proyectos colaborativos con empresas: 5% del costo del proyecto</li>
              <li>Cursos de capacitación por persona:
                <ul>
                  <li>Básicos: $ 150.00</li>
                  <li>Intermedios: $250.00</li>
                  <li>Avanzados y especiales: $ 350.00</li>
                </ul>
              </li>
              <li>Certificaciones Latinux: $ 150.00 Dólares Americanos</li>
              <li>Asesorías y soporte técnico: Sin costo exceptuando refacciones y accesorios</li>
            </ul>
          </p>
