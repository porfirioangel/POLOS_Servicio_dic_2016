        <h1 class="text-center">Objetivos</h1>
        <div class="col-md-10 col-md-offset-1 text-justify">
          <br>
          La implantación de los Polos de Innovación Tecnológica requiere asumir un plan estratégico que permita llevar sus
          beneficios a regiones marginadas de los desarrollos tecnológicos de municipios urbanamente más avanzados, deben de
          consolidarse como una instancia especializada en cada una de las regiones además basándose en los siguientes objetivos:
          <br><br><br>
          <h2>1. Desarrollar proyectos y soluciones tecnológicas.</h2>
          Analizar, diseñar y desarrollar proyectos que resuelvan las necesidades de organizaciones, instituciones de
          educación y empresas de cualquier sector productivo de las regiones donde se ubiquen los Polos; basando todos
          los proyectos en software libre y código abierto, lo anterior genera un beneficio tecnológico y con un ahorro
          económico de más del 60% en comparación con soluciones licenciadas con software privativo; el desarrollo de los
          proyectos serán realizados de forma aliada con empresas de TI de la región fomentando el desarrollo económico.
          <br><br><br>
          <h2>2. Establecer relación con empresas de TI de la región.</h2>
          Si bien los Polos de Innovación Tecnológica pretenden ser instancias especializadas en temas de innovación
          tecnológica es importante contar con la integración al modelo de desarrollo de cada polo de las empresas de base
          tecnológica de cada región; con esto se busca impulsar el desarrollo económico de cada región además de que las
          empresas locales se beneficien integrándose a proyectos importantes.
          <br><br><br>
          <h2>3. Fortalecer la vinculación con instituciones de educación de la región</h2>
          En diversos municipios del Estado se cuenta con instituciones de educación media supeior y superior que brinda el
          increíble potencial integrando a todos aquellos estudiantes que, como parte de sus actividades académicas,
          deben realizar actividades como servicio social, estadías, prácticas profesionales, residencias profesionales y hasta
          proyectos de titulación; esta vinculación fortalece tanto las capacidades de los Polos como también se comienza a
          impulsar el emprendimiento entre los jovenes estudiantes para que creen sus propias empresas de servicios y
          soluciones tecnológicas.
          <br><br><br>
          <h2>4. Adoptar soluciones tecnológicas desarrolladas por el Laboratorio de Software Libre e instancias colaboradoras.</h2>

          El Laboratorio de Software Libre del COZCyT a escasos 2 años de creado, con un promedio de 49 estudiantes por
          semestre que se integran a colaborar en proyectos de innovación tecnológica y con un catálogo actualmente de
          mas de 60 proyectos desarrollados y listos para implantarse; lo anterior además de las importantes alianzas con
          instancias especializadas en desarrollos de proyectos tecnológicos con software libre y código abierto ofrecen a
          los Polos de Innovación Tecnológica la increíble ventaja de contar con un basto catalogo de proyectos que se
          pueden aprovechar para cumplir la misión para la que serían creados.
          <br><br><br>
          <h2>5. Crear planes completos de capacitación, especialización y eventos públicos de difusión tecnológica</h2>
          Una función prioritaria sin duda es la de difundir de manera pública las tendencias tecnológicas que pueden ser
          aprovechadas para beneficio de todos, además de crear planes de capacitación y especialización diseñados de
          manera especial para cada región del Estado; con esto los Polos deben de consolidar la cultura hacia el
          aprovechamiento de las tecnologías actuales, sus soluciones y servicios.
          <br><br><br>
          <h2>6. Impulsar el desarrollo de proyectos tecnológicos para fortalecer tecnológicamente los sectores productivos de las
          regiones</h2>
          De manera especial, los Polos de Innovación Tecnológica deben de involucrarse con las necesidades especificas de
          cada sector productivo en cada región donde se encuentren ubicados; con esto trabajar en el diseño y desarrollo
          de proyectos para la aplicación de las tecnologías en necesidades específicas.
          <br><br><br>
          <h2>7. Impulsar el desarrollo de nuevas empresas de TI y emprendimiento fortaleciendo el desarrollo económico</h2>

          Sin excepción alguna, las instituciones de educación entregan a la sociedad profesionistas con una formación
          académica extraordinaria que cuentan con toda la capacidad para generar sus propias empresas; los Polos de
          Innovación Tecnológica deben ofrecer a estos jóvenes profesionistas capacitación y especialización en temas de
          gestión empresarial, administración de proyectos y emprendimiento; además de fortalecer sus capacidades
          técnicas con capacitaciones y certificaciones.
          <br><br><br>
          <h2>8. Consolidarse como un centro especializado de asesoría y respaldo tecnológico</h2>

          Todas aquellas empresas, organizaciones, instancias de gobierno y la misma sociedad de las regiones donde se
          ubiquen los Polos de Innovación Tecnológica deben encontrar es estos una instancia pública y abierta para contar
          con asesorías, soporte técnico y tecnológico necesario para el desarrollo de proyectos y fomento a la cultura
          tecnológica en general.
         <br><br><br><br><br>
