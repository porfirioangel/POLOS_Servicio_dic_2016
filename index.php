    <h1>Polos de Innovación Tecnológica</h1>
    <p class="text-justify">
      La intención por impulsar la innovación tecnológica ha generado amplios debates políticos y académicos, sobre todo por la
      importancia en su desarrollo de la participación de las organizaciones, instituciones e instancias públicas. Es importante
      analizar el escenario actual iniciando con una reflexión sobre el proceso de innovación en las organizaciones, después,
      profundizando en aspectos conceptuales de las redes en dirección hacia un conocimiento amplio. El concepto de progreso
      económico en las economías capitalistas sobre el impacto de las innovaciones tecnológicas se contraponen a la idea del
      equilibrio general de la economía tal como se describe en la economía neoclásica, (Schumpeter, 1912).
      El concepto de Polo Tecnológico o Tecnopolo surgio en los 80's y fue idealizada para convertirse en una ciudad del futuro
      en medio en un campo de girasol en Poitiers, Francia. En San Leopoldo, Campinas, Brasil; fue creado el Polo informático en
      1999 con la finalidad acoger empresas de base tecnológica y generar un ambiente de sinergia entre dichas empresas,
      gobierno y el resto de los sectores de la región. Su estructura engloba una incubadora empresarial, un edificio de empresas
      y un parque tecnológico.

    </p><br>
